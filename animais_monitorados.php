<h4 align='center' class="display-5 mb-4" style="font-size: 30px">Animais monitorados</h4>

<p class="styled-text">
    Atualmente o projeto Pegadas da Serra foca na captura de três espécies de mamíferos carnívoros: Lobo-guará (<i>Chrysocyon brachyurus</i>),
    Onça parda (<i>Puma concolor</i>) e a Jaguatirica (<i>Leopardus pardalis</i>).
    Os animais estão sendo capturados no âmbito do Programa Pegadas da Serra como uma das vertentes de estudos dos componentes de biodiversidade mantidos pela Mineração Usiminas.
    Para as capturas são utilizadas armadilhas de livre desarme, do tipo Live-Box (gaiolas teladas confeccionadas de acordo com os requerimentos da espécie).
    Os locais de armadilhagem foram selecionados a partir da presença de vestígios, como rastros e fezes e dos resultados das armadilhas fotográficas. 
    As armadilhas foram iscadas com frango cozido e, por vezes, com frutas da estação, e checadas diariamente pela manhã. Para felinos estão sendo utilizadas iscas vivas seguindo protocolos corretos da licença ambiental.  
</p>
<p class="styled-text">
    A coleta  de dados é realizada através do uso de radio-colares equipados nos animais, que através de um GPS aclopado coletam pontos com coordenadas geográficas dos animais monitorados.
    Desta maneira a pesquisa pode acompanhar os movimentos dos bichos, permitindo realizar inferências sobre a vida dos carnívoros e sua relação com o ambiente em seu entorno.
</p>
