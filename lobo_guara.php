<h4 align='center' class="display-5 mb-4" style="font-size: 30px">Lobo-guará (<i>Chrysocyon brachyurus</i>)</h4>

<p class="styled-text">
    O lobo guará é o maior canídeo sul-americano, é solitário e se junta em casais apenas na época reprodutiva.
    Mede entre 95 e 115 cm de comprimento (mais 38 a 50 cm de cauda) e pesa entre 20 e 30 kg.
    Seu padrão de atividade é predominantemente crepuscular ou noturno. Apresenta dieta onívora, constituída basicamente de frutos e pequenos vertebrados, em proporção aproximada de 50% para cada categoria.
    Desempenha importante função ecológica tanto na regulação de populações de suas presas quanto na dispersão de sementes de muitas espécies do Cerrado.
    A distribuição do lobo-guará no Brasil abrange até a floresta Amazônica no norte/noroeste e a Caatinga no nordeste. Mas sua ocorrência em áreas consideradas florestais pode expandir à medida que o desmatamento avança, mudando a paisagem. Este canídeo pode ser mais facilmente encontrado em habitats abertos como cerrados, campos e áreas alagáveis (Paula & DeMatteo, 2015).
    O lobo-guará é considerado vulnerável no Brasil e está listado como “near threatened” na lista da IUCN. A ameaça mais
    significativa para populações de lobos-guarás é a drástica redução e fragmentação de habitats, especialmente devido à conversão de áreas naturais em lavouras.
</p>
