<h4 align='center' class="display-5 mb-4" style="font-size: 30px">Áreas de vida (resultados parciais)</h4>
<div style="columns: 2; column-gap: 40px; ">
    <div style="border-right: 1px solid #ccc; padding-right: 10px;">


    <p>A confecção resultados finais da primeira fase do projeto Pegadas da Serra estão desenvolvimento, os resultados aqui descritos estão em formato de resumo.  </p>
    <p> As análises foram realizadas através dos softwares R (Development Core Team 2009) e QGIS (QGIS 2009). As áreas de vida foram calculadas por meio do menor polígono convexo formado pelas localizações (100% dos pontos), método mais utilizado para comparações com outros estudos. Também foi utilizado o método de densidade de Kernel (WORTON 1989), que calcula a intensidade de uso da área, as duas metodologias portanto complementares. Em sequência, os pontos foram plotados em imagens de satélite para observações iniciais a respeito dos padrões de movimento do animal.

 </p>
