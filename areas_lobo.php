    <p> Neste caso, os resultados demonstram que Sirius movimenta-se por entre e no interior dos taludes produzidos pela atividade mineradora, sugerindo que estas áreas não impossibilitam seu deslocamento. Os resultados preliminares sugerem que o animal utiliza de forma intensa as distintas vertentes da Serra, norte e sul. Outro dado muito importante obtido, é que foi evidenciado que este lobo atravessa a rodovia MG 431 (Itatiaiuçu – Itaúna) no período da noite. Foi também observado que o animal aproxima-se de áreas urbanizadas do município de Itatiaiuçu, em pontos limítrofes de loteamentos  </p>
