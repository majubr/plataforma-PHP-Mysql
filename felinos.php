<h4 align='center' class="display-5 mb-4" style="font-size: 30px">Onça parda (<i>Puma concolor</i>)</h4>

<p class="styled-text">
    A onça-parda é um grande predador de topo, sendo o mamífero terrestre com a maior distribuição nas Américas, ocorrendo desde o Canadá até o Chile. O seu tamanho corporal pode variar com a latitude de ocorrência. Próximo do equador seu tamanho atinge as menores médias (< 35 kg), mas em latitudes mais altas, a espécie pode pesar mais de 50 kg. A espécie pode ocorrer em uma grande variedade de habitats, como formações 19 florestais, savanas e desertos, e demonstra preferência por ambientes com vegetação baixa e densa. A onça-parda possui hábitos predominantemente noturnos. Devido à sua grande distribuição, a onça-parda possui uma ampla diversidade de presas. Populações em regiões frias e mais distantes do equador, possuem certa preferência por presas maiores (> 30 kg),
        especialmente ungulados, mas populações com distribuição em áreas tropicais tendem a consumir presas menores.
        A espécie sofre com perda de vegetação nativa, atropelamentos, e também por pressão de caça sobre suas presas.
        Pode sofrer retaliação por predação em criações domésticas.
        No Brasil a onça-parda é classificada como vulnerável (MMA, 2014), e listada como “least concern” na IUCN.
</p>
<h4 align='center' class="display-5 mb-4" style="font-size: 30px">Jaguatirica (<i>Leopardus pardalis</i>)</h4>

<p class="styled-text">
    A jaguatirica é um dos carnívoros mais bem estudados do Brasil. Considerada um felino de médio porte, seu peso corporal pode atingir de 8 a 16 kg. As jaguatiricas são relativamente fáceis de serem individualizadas, através dos padrões de manchas negras formando rosetas abertas com listras longitudinais nas laterais (figura 1). Este gato é solitário com hábitos predominantemente noturnos, mas podendo apresentar atividade diurna em algumas regiões (Emmons, 1987; Murray & Gardner, 1997). Sua dieta é ampla, consumindo pequenos mamíferos, aves, répteis e anfíbios. Pode apresentar preferências por presas com mais de 1 kg, e por regiões com alta densidade de roedores.
    A espécie pode ser encontrada no sul estado Texas nos EUA, América Central, e se distribuindo até o norte da Argentina na América do Sul.
    A jaguatirica tem potencial para ocorrer em diversas variações de formações florestais (tropicais, subtropicais, semidecíduas e decíduas),
    em savanas, campos e regiões semiáridas.
    Mas aparentemente apesar da ampla distribuição, parece estar mais associada a ambientes com densa cobertura vegetal como florestas tropicais e subtropicais.
    Pode ser encontrada também em áreas perturbadas e fragmentos florestais. A jaguatirica não é considerada uma espécie ameaçada. É listada como “least concern” pela lista brasileira de espécies ameaçadas de extinção (MMA, 2014) e pela IUCN.
    As maiores ameaças para a espécie são perda e fragmentação de habitat, retaliação por abate em aves domésticas e caça ilegal.
</p>
