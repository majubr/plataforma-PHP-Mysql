<h4 align='center' class="display-5 mb-4" style="font-size: 30px">Análise de dados</h4>

<p class="styled-text">
A presença de mamíferos predadores na região do monitoramento se destaca pois são animais que desempenham importantes funções ecológicas para a manutenção dos ecossistemas onde estão inseridos. 
Os carnívoros, principalmente espécies com grande área de vida, se encaixam bem no conceito de “espécies guarda-chuva”, que são aquelas cujas ações de conservação, acabam por conservar outras espécies dentro de sua abrangência espacial 
</p>
<p class="styled-text">  
A mesma espécie que se enquadra neste conceito anterior, também pode ser considerada uma “espécie-chave”, se ela tiver uma grande importância na regulação de determinado ecossistema. 
Os carnívoros podem ser incluídos nestes dois conceitos, pois exercem importante função dentro do ecossistema, influenciando a abundância de suas presas, podendo também até mesmo afetar a diversidade biológica da sua comunidade 
</p>
<p class="styled-text">  
