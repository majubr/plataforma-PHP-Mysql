<p class="styled-text">
  O Banco de Dados de Fauna da Mineração Usiminas, BDFauna – Musa, é direcionado à organização, sistematização, armazenamento e disponibilização de informações sobre os componentes da fauna da região de Serra Azul MG.
  O Sistema de Informação do BDFauna – MUSA serve de apoio e subsídio à gestão ambiental para a tomada de decisão relativas às práticas de manejo e conservação da diversidade biológica regional.
  O Sistema disponibiliza o acesso a informações atualizadas, dispondo de ferramentas de busca, consulta, manuseio de informações, acesso a métricas, adaptação de dados para análises diversas, emissão de relatórios de dados. Enquanto uma coleção organizada de informações, no BDFauna os dados individuais de cada projeto estão interligados por meio de padrões, protocolos e intercâmbio de dados que, juntamente com programas aplicativos, permite a geração de informações analíticas particularizadas.
  Os dados depositados no BDFauna seguem um formato de organização denominado Darwin Core, o mesmo usado pelo Sistema de Informação sobre a Biodiversidade Brasileira (SiBBr) e por sua vez com o Global Biodiversity Information Facility GBIF, permitindo assim o compartilhamento de dados em múltiplas escalas.
